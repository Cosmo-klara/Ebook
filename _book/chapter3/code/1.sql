use test3;

CREATE TABLE students
(
    stu_id  INT PRIMARY KEY,
    sname   VARCHAR(8),
    sgender CHAR,
    sage    TINYINT,
    sclass  TINYINT,
    smajor  VARCHAR(16)
);

CREATE TABLE teachers
(
    tch_id  INT PRIMARY KEY,
    tname   VARCHAR(8),
    tgender CHAR,
    tage    TINYINT
);

CREATE TABLE courses
(
    c_id    INT PRIMARY KEY,
    cname   VARCHAR(16),
    credits TINYINT,
    ct_id   INT,
    CONSTRAINT 授课教师编号 FOREIGN KEY (ct_id) REFERENCES teachers (tch_id)
);

CREATE TABLE records
(
    rstu_id INT,
    rc_id   INT,
    grade   TINYINT,
    CONSTRAINT 选课学生编号 FOREIGN KEY (rstu_id) REFERENCES students (stu_id),
    CONSTRAINT 课程编号 FOREIGN KEY (rc_id) REFERENCES courses (c_id)
);


